package brasileirao.model;

public class MatchDate
{
	public final int day;
	public final int month;
	public final int hour;
	public final int minute;

	public MatchDate(int day, int month, int hour, int minute)
	{
		this.day = day;
		this.month = month;
		this.hour = hour;
		this.minute = minute;
	}
}